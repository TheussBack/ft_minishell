#include "minishell.h"

int	my_pwd(char *pwd)
{
	if (getcwd(pwd, 2048))
	{
		ft_putstr_fd(pwd, 1);
		ft_putstr_fd("\n", 1);
		return (0);
	}
	return (1);
}
